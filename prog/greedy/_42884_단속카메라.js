function solution(routes) {
    routes.sort(ASC);

    let answer = 0;
    let camera = -30_001;
    routes.forEach(([start, end]) => {
        if (start > camera) {
            camera = end;
            answer++;
        }
    });
    return answer;
}

const ASC = ([, endA], [, endB]) => {
    return endA - endB;
};

