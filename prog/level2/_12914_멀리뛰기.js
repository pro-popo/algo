function countJump(n) {
    const dp = Array(n + 1).fill(null);
    dp[0] = dp[1] = 1;

    return fibonacci(n);

    function fibonacci(n) {
        if (dp[n]) return dp[n];
        return (dp[n] = (fibonacci(n - 1) + fibonacci(n - 2)) % 1234567);
    }
}

